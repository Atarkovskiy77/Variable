my_string = str(input('Введите название предмета: '))
print(len(my_string)) # Вывод количества символов введенного текста
print(my_string.upper()) # Вывод строки в верхнем регистре
print(my_string.lower()) # Вывод строки в нижнем регистре
print(my_string.replace(' ','')) # Вывод строки с удалёнными пробелами
print(my_string[0]) # Вывод первого символа строки
print(my_string[-1]) # Вывод последнего символа строки